package com.example.studey.java.demo1.Method;

public class Day05For {
    public static void main(String[] args){
        /*格式：
        * for (初始化语句1；条件判断语句2；循环体执行之后的语句4){
        *           循环体3；
        * }
        * */
        //输出1-5
        //debug 调试
        for (int i=1;
             i<=5;
             i++){
            System.out.println(i);
        }

    }

}
