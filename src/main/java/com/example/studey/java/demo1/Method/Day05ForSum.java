package com.example.studey.java.demo1.Method;

public class Day05ForSum {


    public static void main(String[] args) {

        //循环中的求和思想
        //求1+2+3+4+5
        int sum = 0;
        for (int i = 1; i <= 5; i++) {
            sum = sum + i;
        }
        System.out.println(sum);

    }
}
